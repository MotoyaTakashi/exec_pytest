


def checkA( sentence ):
    """
    文字列に "A" が含まれていれば 1、
    文字列に "B" が含まれていれば 2、
    文字列に "C" が含まれていれば 3、
    いずれも含まれていなければ 0 を返す
    """

    if "A" in sentence:
        return 1

    elif "B" in sentence:
        return 2

    elif "C" in sentence:
        return 3

    else:
        return 0



